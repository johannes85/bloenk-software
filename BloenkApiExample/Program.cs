using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using System.Drawing;
using Bloenk;

namespace ApiExample
{
    class Program
    {
        static void Main(string[] args)
        {
            BloenkDevice bloenkDevice = new BloenkDevice(5824, 1500);
            bloenkDevice.OpenDevice();
            bloenkDevice.SetColor(1, Color.FromArgb(255, 255, 0));
            bloenkDevice.Write();
            bloenkDevice.CloseDevice();
        }
    }
}
